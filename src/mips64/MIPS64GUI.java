    private void Run2BreakButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Run2BreakButtonActionPerformed
        // TODO add your handling code here:
        try {
            int breakAddress = Integer.parseInt(BreakAddressField.getText());
            if (breakAddress >= 0 && breakAddress % 4 == 0) {
                simulator.step();

                while (!simulator.memWb.isHalted() && simulator.pc.getPC() != breakAddress) {
                    simulator.step();
                }
                updateView();
                Run2BreakButton.setSelected(false);
            }
        } catch (NumberFormatException nfe) {
        }
    }//GEN-LAST:event_Run2BreakButtonActionPerformed

    private void Run2CountButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Run2CountButtonActionPerformed
        // TODO add your handling code here:
        try {
            int breakCount = Integer.parseInt(RunToCountField.getText());
            if (breakCount > 0) {
                simulator.step();
                int count = 1;
                while (!simulator.memWb.isHalted() && count < breakCount) {
                    simulator.step();
                    count++;
                }
                updateView();
                Run2CountButton.setSelected(false);
            }
        } catch (NumberFormatException nfe) {
        }
    }//GEN-LAST:event_Run2CountButtonActionPerformed

