package mips64;

public class IntOrFloat {
  int myInt;
  float myFloat;

  public IntOrFloat() {
  }

  public int getInt() {
    return myInt;
  }
  public void setInt(int newInt) {
    myInt = newInt;
  }

  public float getFloat() {
    return myFloat;
  }
  public void setFloat(float newFloat) {
    myFloat = newFloat;
  }

}
